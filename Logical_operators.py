#En el operado AND las dos condicones deben de ser positivas para que el resultado sea true
print (5 > 2 and 9 < 11)

#En caso de no cumplir nos devolvera false
print (5 > 2 and 10 < 2)

#En el operador OR las dos condiciones no deben de ser positivas para que el resultado sea true
print (5 > 2 or 10 < 3)

#En caso de no cumplir nos devolvera false
print (5 > 19 or 10 < 3)

#En el operador not sera el cambio si en este caso es true mostrara false y visebersa
print (not (5 > 7) )
